  static List<WisdomItem> getWisdomForContext(UserContext context) {
    return allWisdom.where((item) {
      return item.isSuitableForTime(context.timeOfDay) &&
             item.isSuitableForMood(context.inferredMood) &&
             item.isSuitableForActivity(context.activityContext);
    }).toList();
  }

  static List<WisdomItem> getWisdomForTimeOfDay(TimeOfDayPeriod period) {
    return allWisdom.where((item) => item.isSuitableForTime(period)).toList();
  }

  static List<WisdomItem> getWisdomForMood(UserMood mood) {
    return allWisdom.where((item) => item.isSuitableForMood(mood)).toList();
